    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String msg = null;
      
        if(!txtQuantidade.getText().equals("")){
            try {
                if(helper.verificaEstoque()){
                    ProdutoVenda pv = helper.getProdutoVenda();
                    
                    controller.addItemVenda(pv);
                    
                    
                    // continua
                } else{
                    JOptionPane.showMessageDialog(null, "S√≥ esta disponivel "+lblQtdDisponivel.getText()+" unidades deste item!", "Aviso", JOptionPane.WARNING_MESSAGE);
                }
            } catch (NumberFormatException e){
                if (msg == null) msg = "Quantidade precisa ser um numero inteiro";
                else msg += "\nQuantidade precisa ser um numero inteiro";
                txtQuantidade.setText("");
            }
        } else{
            msg = "Voc√™ precisa preencher o campo de quantidade!";
        }
        
        if(msg != null) JOptionPane.showMessageDialog(this, msg);
    }//GEN-LAST:event_btnRegistrarActionPerformed

